//Exibindo textos no JS
//O JavaScript é uma linguagem de programação de tipagem dinâmica, ou seja, o motor de processamento do JavaScrip já tem valores setados, ao usar aspas simples ou duplas o valor do dado já é considerado automaticamente como String
console.log("Mateus Canjerana");        //Aspas duplas podem ser usadas para exibir textos  
console.log("Mateus 'Canjerana'");      //Pode-se colocar aspas simples dentro de aspas duplas

console.log('Mateus Canjerana');        //Aspas simples podem ser usadas para exibir textos
console.log('Mateus "Canjerana"');      //Pode-se colocar aspas duplas dentro de aspas simples

console.log(`Mateus Canjerana`);        //Pode-se usar crases para exibir textos também
console.log(`"Mateus" 'Canjerana'`);    //Dentro da crase pode-se usar aspas simples e aspas duplas

//Exibindo números 
//No JavaScript não tem separação para tipos de números, tanto o que seria um int '12' quanto o que seria float '7.5', são considerados como Number
console.log(123456, 15.765, 78, 'Mateus');            //Digite literalmente o valor desejado


